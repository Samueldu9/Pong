    public void keyPressed(KeyEvent e) //Presionamos la tecla
    {
       if (e.getKeyCode()==87)
       {
           paletai-=velocidad;
       }
       else if (e.getKeyCode()==83)
       {
           paletai+=velocidad;
       }
       
       if (e.getKeyCode()==38)
       {
           paletad-=velocidad;
       }
       else if (e.getKeyCode()==40)
       {
           paletad += velocidad;
       }
       System.out.println(e.getKeyCode());
       
    }

    
